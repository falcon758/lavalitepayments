<?php

namespace Shopping\Payments\Providers;

use Illuminate\Contracts\Auth\Access\Gate as GateContract;
use Illuminate\Foundation\Support\Providers\AuthServiceProvider as ServiceProvider;

class AuthServiceProvider extends ServiceProvider
{
    /**
     * The policy mappings for the package.
     *
     * @var array
     */
    protected $policies = [
        // Bind Payment policy
        'Shopping\Payments\Models\Payment' => \Shopping\Payments\Policies\PaymentPolicy::class,
// Bind Transaction policy
        'Shopping\Payments\Models\Transaction' => \Shopping\Payments\Policies\TransactionPolicy::class,
    ];

    /**
     * Register any package authentication / authorization services.
     *
     * @param \Illuminate\Contracts\Auth\Access\Gate $gate
     *
     * @return void
     */
    public function boot(GateContract $gate)
    {
        parent::registerPolicies($gate);
    }
}
